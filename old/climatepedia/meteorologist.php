<a name="education"></a>
<h4>Education</h4>
<p>Whether you’re entering high school or graduating from college, it’s always the right time to focus in on what your education needs to be for you to accomplish your goals. The basic educational requirement for a non-research meteorologist is a bachelors of science in a related field with having taken meteorology courses. If you plan on having any career advancement you should get your masters of science degree in meteorology as early as possible, and if you plan on participating in any cutting-edge research or teach at a university you’ll need your Ph.D.</p>

<a name="highschool"></a>
<h5>High School</h5>

<p>While in high school you want to focus on mathematics and other science courses, but always remember that all of your courses matter. Do well in general in school, take on extracurricular activities that may or may not be related to meteorology, and take advanced courses. Once you’re in college and in later education you’ll have time to really get down to the nitty-gritty. </p>
<p>You should make <img src="css/images/pedia/weather.png" width="400" height="263" alt="meteorologists can forecast the weather to help others prepare" align="right" class="wrapright" >sure to get in contact with your guidance counselor as soon as possible to plan out where you want to go to college, and what you need to do GPA and SAT-wise to get there. There are very few colleges that offer a bachelors of science in meteorology, so you probably don’t want to just wing it. You can also major in a field of science related to meteorology (physics, mathematics, etc.) and still enter the meteorology field, though you will have to take the basic undergraduate meteorology courses regardless.</p>
<p>As the later years of high school come around you may want to become familiar with the different fields of meteorology and the prominent figures within each. If your chosen career path requires post-baccalaureate study you should check to make sure the schools you apply to offer the courses that are required by masters programs. Alternatively, if you plan on going straight into work with the National Weather Service you should check to make sure that your future college offers the courses the NWS looks for in their junior meteorologists.</p>


<a name="bachelors"></a>
<h5>Undergraduate Study (Bachelor's Degree)</h5>
<p>Once you’ve gotten into your undergraduate college of choice, start planning out your specialization a bit more. This won’t seem as important during your early years in college, but in truth this is the time when you lay the foundation for all the great things you’ll do your junior and senior year. </p>
<p>During the first quarter/ semester of school all of the on-campus organizations will be out in strong trying to make you aware of their cause so that you may join, which  makes for a great time for you to identify the organizations you think you’d like to be involved with throughout your time in college. If you join an organization or club as an underclassman then by the time you’re an upperclassman you’ll be ready to take leadership, making for a great experience to put on your C.V./ Resume/ graduate study applications.</p>
<p>As you join these clubs and get some experience with what type of subject material you’re most interested in, you may want to tailor your studies to fit your career goals. For example, if you want to be a television weather person you should pick up either a minor or double major in journalism, communication studies, etc, or you may want to join the school newspaper. If you would rather do climate research for a laboratory you may want to get some experience with programming and database management, as you may some day soon be handling huge amounts of data.</p>
<p>Many meteorological organizations also offer student internships or co-ops over the summer or during a school term, which would be the best type of experience for you to get prior to graduation.</p>

<a name="preparation"></a>
<h4>Employment Preparation</h4>

<a name="experience"></a>
<h5>Getting Experience</h5>
<p>You can get experience in the field by setting up your own amateur weather station or volunteering at a local weather station.</p>

<a name="internships"></a>
<h5>Meteorologist Internships</h5>
<p>Many private and government organizations offer internships for meteorology students to get some experience in the workplace before graduation. For example, the American Meteorological Society has an excellent page detailing opportunities around the United States here: http://www.ametsoc.org/amsstudentinfo/internships.html. </p>
<p>You may find internships that require a college degree and those that don’t, aim for whatever fits your situation best. If you live in a large city you should check with your local broadcasting company or station to see what opportunities they offer.</p>


<a name="desired"></a>
<h5>Desired Qualities</h5>
<p>The desired “soft” qualities for meteorologists is similar to most jobs, here’s a list of the types of things employers look for:</p>
<ul class="list">
<li>Proven ability to work in a team</li>
<li>Good written and verbal communication skills</li>
<li>High energy and motivation</li>
<li>Responsibility</li>
<li>Some type of quantitative experience</li>
</ul>

<a name="tips"></a>
<h5>Application Tips</h5>
<p>Make sure you create your resume once you understand what employers are looking for in potential meteorologists; you will want to tailor it to fit your specific niche - i.e. television broadcasting or government research. If you’re asked to email your resume you should probably do so in PDF format to make sure that it retains its formatting and font choices.</p>
<p>Don’t <img src="css/images/pedia/meteorology.jpg" class="wrapleft" align="left" width="400" height="300" alt="a meteorologist is busy working with data and computers"> give up early, meteorology is a competitive field and you are likely to face some rejections before landing a position that you like.</p>
<p>You may want to prepare letters of reference from previous employers or professors.</p>
<p>When you send in an application or write a cover letter, make sure you have a strong “hook” to set you apart from the crowd. This should be specific to whatever field you’re applying to, and nobody can tell you what it should be. For example, if you want to do research for the government, it would be beneficial if you had done something during a hobby of yours that involved number crunching or database management, like compiling your own weather data for your own statistical models. This may sound crazy to some, but it should be second nature for those who wish to spend their careers researching the climate.</p>


<a name="employment"></a>
<h4>Meteorologist Employment</h4>
<p>Meteorologists are needed by a wide array of employers, so you should identify who you’re aiming for at the start. Both public and private employers will be looking for employees, so you could go into research, weather forecasting, military, teaching, television broadcasting, consulting, etc. In general, the private sector will pay a higher salary while the government will give more secure job security; however, this is a sweeping generalization and will not always hold true.</p>

<a name="employers"></a>
<h5>Biggest Employers of Meteorologists</h5>
<p>The United States government employs the greatest number of meteorologists, with most of them in the National Oceanic and Atmospheric Administration (NOAA), which includes the National Weather Service.</p>


