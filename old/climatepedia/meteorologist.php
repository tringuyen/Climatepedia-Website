<a name="overview"></a>

<p>Meteorology (Atmospheric science) is one of the most fascinating scientific subjects and there are many paths one can take within it. A meteorologist may find themselves forecasting the weather, researching weather phenomena, or investigating air pollution or global warming trends, all for either private industry or the federal government. It is a field of science that relies heavily on data, and thus involves plenty of math and computer interfacing.</p>
<p>A wide array of other fields rely on meteorological data, from agriculture to defense and transportation, making the predictions of meteorologists vital in the smooth-running of the world.</p>

<a name="profession"></a>
<h4>The Job - Meteorology as a Profession</h4>
<p>The specific jobs that meteorologists fill are quite varied, some examples are:</p>
<ul class="list">
<li><strong>Operational Meteorologists</strong>: forecast weather and comprise the largest group of meteorologists.</li>
<li><strong>Environmental Meteorologists</strong>: study environmental problems, particularly the air quality of industrialized areas.</li>
<li><strong>Synoptic Meteorologists</strong>: create new tools using mathematical models and computer programs for forecasting the weather.</li>
<li><strong>Climatologists</strong>: study the variations of the climate over the long-term, and are particularly focused on global warming as a trend.</li>
<li><strong>Physical Meteorologists</strong>: research the physical aspects of the atmosphere, such as its chemical composition and electrical characteristics.</li>
</ul>

<a name="interviews"></a>
<h5>Interviews with Prominent Meteorologists</h5>
