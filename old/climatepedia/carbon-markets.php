<a name="tianjin"></a>
<h5>Tianjin Climate Exchange</h5>
<p>The Tianjin Climate Exchange was set up by the State Council as its first national environmental equity trading institution, and its goal is to use market instruments and financial innovation to promote energy conservation. It is a three-way joint venture set up by CNPC Assets Management Co., Ltd, Tianjin Property Rights Exchange, and the Chicago Climate Exchange.</p>
