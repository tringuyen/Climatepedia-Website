<a name="montreal"></a>
<h5>Montreal Climate Exchange</h5>
<p>The Montreal Climate Exchange (MCeX) is a joint venture between the Montreal Exchange (MX) and the Chicago Climate Exchange (CCX) meant to provide a market-based solution to help companies and all those involved in addressing the most serious environmental challenges, especially reducing air pollutant and greenhouse gas emissions. Market participants trade futures contracts on Canada carbon dioxide equivalent units, which allow regulated industrial participants to manage their emissions risks at the lowest cost while also creating continuous incentives for technological innovation.</p>


<a name="envex"></a>
<h5>Envex - Australian Climate Exchange</h5>
<p>Envex is a climate exchange that was developed in aims to serve the environmental markets of Australia and Asia-Pacific by providing both over-the-counter and exchange-traded financial products.</p>
<p>Their plan is to build on existing carbon market structures to enhance transparency and efficiency, develop global linkages and help Australia transition to its proposed Carbon Pollution Reduction Scheme.</p>

<a name="other"></a>
<h5>Other Carbon Allowance Exchanges</h5>
<p>Carbon allowances are also traded on the European Climate Exchange, NASDAQ OMX Commodities Europe, PowerNext, Commodity Exchange Bratislava and the European Energy Exchange.</p>


