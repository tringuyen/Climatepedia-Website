<a name="overview"></a>

<p>Carbon markets are the financial markets where emissions trading, or cap and trade, takes place. <img src="css/images/pedia/cap.jpg" alt="cap and trade scheme emissions trading allowances and money" class="wrapright" align="right" width="350" height="256">Cap and trade is a method of limiting the amount of pollution by commercial enterprises using economic incentives that reward emissions efficiency and allocate a cost to emissions inefficiency.</p>
<p>Emissions trading is a very new concept that is still being formulated as various countries propose their own cap and trade schemes, and many financial exchanges have been established to handle the increased demand for liquidity in carbon markets around the world. Currently the world’s largest emissions trading scheme is the European Union Environmental Trading Scheme. </p>

<a name="carbon"></a>
<h4>Carbon Markets</h4>
<p>The difference between a carbon market and a climate exchange is that a climate exchange is the place in which the trading of emissions contracts is actually facilitated, while a carbon market is created when a government imposes restrictions on company emissions, forcing them to buy allowances from more emissions-efficient enterprises. Thus, the actual market for these contracts is still infantile and depends on a country’s ability to enforce its restrictions on the companies. However, the climate exchanges play a key role in creating a cap and trade system because they provide the liquidity necessary to keep a carbon market moving.</p>

<a name="eu"></a>
<h5>European Union Emissions Trading Scheme (EU ETS)</h5>
<p>The European Union Emission Trading Scheme is largest carbon scheme in the world, and 25 of the 27 European Union countries participate in it.</p>
<p>As<img src="css/images/pedia/eu.jpg" alt="european union emissions trading scheme price drop lack of confidence" class="wrapleft" align="left" width="425" height="354"> the first large-scale cap and trade program, it has had a bumpy ride while working out the kinks. The EU ETS launched in 2005 after a voluntary trial period, but in 2007 the amount of carbon allowances given out exceeded the actual emissions of the year and the price dropped to zero.</p>
<p>In the last year there has been a spike in fraud and theft in the EU ETS, with an estimated $50 billion lost due to computer hackers. Some have suggested that many security holes exist in the emissions trading scheme because each individual country administers its local register, and some countries have stronger cyber defenses than others. </p>

<a name="us"></a>
<h5>United States Cap and Trade</h5>
<p>The United States has faced political difficulty in passing cap and trade legislation, and much of the progress that has been made is due to regional initiatives. In 2008 President Barack Obama confirmed that enacting a cap and trade system was a priority of his administration, but with an influx of Republicans in Congress after the 2010 elections the probability of legislation passing dropped significantly.</p>
<p>Several voluntary carbon markets exist, but without an enforced environmental trading program it is unlikely any real impact on U.S. greenhouse gas emissions.</p>

<a name="ch"></a>
<h5>China Cap and Trade</h5>
<p>China’s government has stated they are set to begin domestic carbon market programs during their next 5-year plan (2011-2015). They have put into motion a pilot program in the Tianjin Binhai New Area, a designated area for financial innovation.</p>

<a name="au"></a>
<h5>Australia Climate Change Legislation</h5>
<p>In 2010 the Australian senate vetoed a bill proposed by the Rudd government to implement an environmental market similar to the European Unions. The prime minister, Kevin Rudd, decided to postpone action regarding the initiative until after the current Kyoto Protocol commitments expire in 2012.</p>
<p>The bill was supposed to reduce Australia’s emissions by 20%, but was also expected to dampen economic growth and lead to slightly higher inflation.</p>

<a name="exchanges"></a>
<h4>Climate Exchanges</h4>
<p>Several climate exchanges have been created in recent years to handle the expected demand for carbon markets as governments pass legislation restricting emissions. There are many exchanges where a variety of financial derivatives are traded, but climate change related ones include: the American Chicago Climate Exchange, the Canadian Montreal Climate Exchange, the Australian Envex Exchange,  the Chinese Tianjin Climate Exchange, and the IntercontintalExchange. These climate exchanges are being created in response to increased government regulations on emissions to combat global warming. </p>

<a name="intercontinental"></a>
<h5>InterncontinentalExchange (ICE)</h5>
<p>IntercontinentalExchange operates leading regulated exchanges, trading platforms and clearing houses serving global markets for agricultural, credit, currency, emissions, energy and equity index markets. In July 2010 it acquired the Chicago Climate Exchange, so its customers can trade carbon allowances on either the Chicago Climate Exchange or its sister company, the European Climate Exchange.</p>

