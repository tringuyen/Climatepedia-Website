<a name="overview"></a>


<p>Climategate is the term dubbed by the media to a controversy in which unidentified persons illegally published material they obtained from a Climate Research Unit online. Some observers declared the emails revealed misconduct within the CRU and climate science community as a whole, but three separate investigations later concluded that there was no evidence of scientific malpractice. </p>

<div id="image">

<img src="css/images/pedia/climategate.jpg" alt="climategate event controversy cartoon" > <p>The Climategate Event spurred a buzz of media humor and controversy. </p>

</div>

<a name="what"></a>
<h4>What Happened?</h4>
<p>It began in mid-November 2009 when a server belonging to the University of East Anglia&#39;s (UAE) Climatic Research Unit (CRU) was accessed, and more than 1,000 emails and 3,000 other documents were stolen and leaked to the internet. A UEA spokeswoman confirmed that the information was not on a server that could be easily accessed and therefore could not have been unintentionally released (1). Allegations were made that climate scientists had manipulated or deleted data in order to support evidence of global warming, and much of the controversy centered on a small number of emails involving Phil Jones, the Director of CRU. </p>

<p>The alarm arose from the fact that the authors of the documents were some of the most important climate scientists behind the claims that man-made emissions were causing global warming (Anthropogenic Global Warming), some playing a role in the UN&#39;s Intergovernmental Panel on Climate Change (IPCC). Professor Philip Jones was responsible for key sets of data used by the IPCC in its reports. During the reviews of the CRU Jones temporarily stood down from his post, and has since been reinstated as Director of Research after he was cleared of the most severe allegations.</p>
<p>Three investigations into the matter rejected these accusations, concluding that there was no evidence of scientific malpractice.</p>

<a name="first"></a>
<h4>First Investigation</h4>
<p>The first reported on 31 March 2010 and found that &quot;the scientific reputation of Professor Jones and CRU remains intact.&quot; It focused on the following&#58;</p>

<ul>

<li>The accuracy and availability of CRU&#39;s data</li>

<li>Allowing access to the data and methodology under the Freedom of Information Act</li>

</ul>

<h6>Their conclusions were as follows&#58;</h6>

<a name="conclusion1"></a>
<p class="quote"><em>136. Conclusion 1&#58;</em> The focus on Professor Jones and CRU has been largely misplaced. On the accusations relating to Professor Jones&#39;s refusal to share raw data and computer codes, we consider that his actions were in line with common practice in the climate science community. We have suggested that the community consider becoming more transparent by publishing raw data and detailed methodologies. On accusations relating to Freedom of Information, we consider that much of the responsibility should lie with UEA, not CRU.</p>
<a name="conclusion2"></a>
<p class="quote"><em>137. Conclusion 2&#58;</em> In addition, insofar as we have been able to consider accusations of dishonesty&#45; for example, Professor Jones&#39;s alleged attempt to "hide the decline"â€”we consider that there is no case to answer. Within our limited inquiry and the evidence we took, the scientific reputation of Professor Jones and CRU remains intact. We have found no reason in this unfortunate episode to challenge the scientific consensus as expressed by Professor Beddington, that "global warming is happening [and] that it is induced by human activity".[184] It was not our purpose to examine, nor did we seek evidence on, the science produced by CRU. It will be for the Scientific Appraisal Panel to look in detail into all the evidence to determine whether or not the consensus view remains valid.</p> 
<a name="conclusion3"></a>
<p class="quote"><em>138. Conclusion 3&#58;</em> A great responsibility rests on the shoulders of climate science&#58; to provide the planet&#39;s decision makers with the knowledge they need to secure our future. The challenge that this poses is extensive and some of these decisions risk our standard of living. When the prices to pay are so large, the knowledge on which these kinds of decisions are taken had better be right. The science must be irreproachable.</p>

