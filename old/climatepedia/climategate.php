<a name="second"></a>
<h4>Second Investigation</h4>
<p>The second investigation was conducted by an independent Science Assessment Panel, and was published on 14 April 2010. The panel was set up by the University of East Anglia in order to &#34;assess the integrity of the research published by the Climatic Research Unit in the light of various external assertions.&#34; Their investigation focused on review of 11 works published by the CRU and evaluation of the integrity of the Unit&#39;s research rather than its correctness. In their report&#39;s conclusion regarding scientific malpractice they stated the following&#58;</p>

<p class="quote">1. We saw no evidence of any deliberate scientific malpractice in any of the work
of the Climatic Research Unit and had it been there we believe that it is likely
that we would have detected it. Rather we found a small group of dedicated if
slightly disorganised researchers who were ill-prepared for being the focus of
public attention. As with many small research groups their internal procedures were rather informal.</p>

<p>They also commented that they were surprised by the fact that &#34;research in an area that
depends so heavily on statistical methods has not been carried out in close
collaboration with professional statisticians...&#34; and that collaboration between the CRU and a broader scientific community outside of their temperature specialty would be mutually beneficial.</p>

<a name="third"></a>
<h4>Third Investigation</h4>
<p>The third investigation was conducted by the Independent Climate Change Email Review, which was funded by the University of East Anglia but was completely independent. In contrast to the previous investigation, this one focused on the hacked email exchanges and other such information to &#34;determine whether there is any evidence of manipulation or suppression of data which is at odds with acceptable scientific practice.&#34; The final report was published on July 7, 2010. In their findings, the Review stated the following&#58;</p>

<p class="quote">13. Climate science is a matter of such global importance, that the highest standards
of honesty, rigour and openness are needed in its conduct. On the specific
allegations made against the behaviour of CRU scientists, <em>we find that their
rigour and honesty as scientists are not in doubt.</em></p>

<p class="quote">14. In addition, we do not find that their behaviour has prejudiced the balance of
advice given to policy makers. In particular, <em>we did not find any evidence of
behaviour that might undermine the conclusions of the IPCC assessments.</em></p>

<p class="quote">15. <em>But we do find that there has been a consistent pattern of failing to display
the proper degree of openness</em>, both on the part of the CRU scientists and on the
part of the UEA, who failed to recognise not only the significance of statutory
requirements but also the risk to the reputation of the University and, indeed, to
the credibility of UK climate science.</p>

