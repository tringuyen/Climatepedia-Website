<a name="overview"></a>

<p>A climatologist is an atmospheric scientist who studies weather conditions over a period of time, not to be confused with a meteorologist. While meteorologists predict weather phenomena and patterns in the relative short-term, climatologists study climate trending over months, years, or centuries. However, much of the foundational education is the same and later specialization differentiates the two paths. A career in climatology could take you from the warm waters of South America to the sub-zero tundra of the poles.</p>
<p>Observance of the weather has existed as an occupation for as long as humanity itself, though it certainly has taken a more scientific turn lately when compared with the mystic sages of ancient societies. Today essentially every aspect of our day-to-day life is impacted by changing conditions, and it&#39;s a climatologist&#39;s job to look at the big picture of our world and the way we are interacting with our Earth. What happens in this big picture affects the plants that can grow in different areas, food production, survival of endangered species, air-pollution control, a population&#39;s energy usage, life expectancy, and ultimately some suggest even the long-term survival of our own species &#40;though this is highly debated, and this article does not take a stance on it&#41;.</p>
<p>A Climatologist gathers climate data and information, investigates climate signs, and analyzes these sources in order to make predictions about climate patterns. As any other data-centric field with many variables, a climatologist will use computer models extensively, specifically to study how the Earth&#39;s climate changes over time. In regards to anthropogenic global warming, climatologists will conduct research to analyze human&#39;s impact on Earth&#39;s current and future climate. Some Climatologists also study the climates of other nearby planets.</p>

