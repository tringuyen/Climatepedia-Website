<h4>Education</h4>

<p>Education is very important, and this section needs to be completed.</p>

<a name="highschool"></a>
<h5>High School</h5>
<p>
	If you&#39;re aiming for a career in climatology you should choose physical science and advanced mathematics courses when you have the opportunity, as well as develop your computer skills and ability to write clearly and effectively. Beyond this just do your absolute best in all of your classes to give yourself the most choices when it comes time to find a college that fits you well.
</p>

<a name="bachelors"></a>
<h5>Undergraduate Study (Bachelor&#39;s Degree)</h5>
<p>
	The minimum requirement for any climatologist position is usually a bachelor&#39;s degree in climatology, meteorology, or another atmospheric science, though many may come from closely related fields. If you do choose a related field (see below), and you want to go on to graduate school, you may need to take extra classes to ensure you have the necessary core meteorology knowledge.
</p>
<p>
	For example, the United States Bureau of Labor Statistics states that the desired educational background for entry-level atmospheric scientists in the Federal Government is a bachelor&#39;s degree and at minimum 24 term hours of meteorology or atmospheric science courses, as well as:
</p>

<ul>
<em> 9 term hours in:</em>
<li>
	subjects like computer science, statistics, chemistry, oceanography, climatology, hydrology, aeronomy, light and optics, electricity and magnetism, etc.
</li>
<em>6 term hours in:</em>
<li>
	analysis and prediction of weather systems
</li>
<li>
	atmospheric dynamics and thermodynamics
</li>
<li>
	college physics
</li>
<em>3 term hours in:</em>
<li>
	physical meteorology
</li>
<li>
	ordinary differential equations
</li>
</ul>

<p>
	<em>Fundamental School Subjects:</em> A lot of Math (algebra, trigonometry, calculus), Geography, Statistics, Computer Science, Physics, Meteorology, Agriculture.
</p>
<p>
	<em>Special School Subjects:</em> If you know what area you want to specialize in climatology you should take courses that will be relevant to your choice. For example:
</p>
<ul>
<li>
	Climatologists who want to work as consultants for businesses would do well to take business, economics, and statistics courses
</li>
<li>
	Those who wish to work for a publication or news station would benefit from journalism, English, or rhetoric classes.
</li>
<li>
	If you want to work as a climatologist in air quality control for the government, you could take extra courses in chemistry and political science.
</li>
</ul>

<p>
	<em>Related Undergraduate Majors:</em> Mathematics, physics, engineering, Geophysics, Environmental Science, Environmental Engineering, Geography, Hydrology, Geotechnical an Geoenvironmental Engineering,, Earth-Environmental Systems (EES).
</p>

