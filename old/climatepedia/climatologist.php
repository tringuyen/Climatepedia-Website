<a name="profession"></a>
<h5>Climatology as a Profession</h5>

<p>
	Climatology looks at the trend of the entire Earth over thousands of years, so valuable data can come from any area. Paleoclimatology is the specific field that focuses on ancient weather patterns.
</p>
<p>
	Climatologists will look at ocean water temperatures, ocean current changes, glacial ice cores, cloud cover, lake sediments, tree rings, atmospheric conditions, solar conditions, etc., as well as less obvious sources such as plant or animal migrations or living habits. For example, if a certain plant has migrated either towards the polar caps or higher in altitude, it may be a sign to climatologists that their normal habitat has been warming. 
</p>
<p>
	Climatologists will monitor atmospheric conditions and cloud cover like meteorologists, but also pull data from a variety of other less obvious sources. For example, climatologists can study ocean temperatures and currents, which have a correlation with long-term climate conditions. They can also study mud from ancient lakes and ice cores from the poles to find evidence of sediments and other compounds which have been caught and preserved. By doing so they may see what climate conditions were like far in the past.
</p>
<p>
	Climatologists have to consider every angle of the story though, as the Earth is an amazingly complex system, so they may draw from more unexpected sources. As an example, a researcher may gather and pour through many landscape paintings and old texts or stories from previous societies in order to ascertain typical cloud cover, environmental events, and whether any indicator plant species existed in that area.
</p>
<p>
	In relation to anthropogenic climate change, or global warming, you will most likely study the effect of both man-made and naturally occurring greenhouse particles on the climate. Climatologists are the scientists who do their best to predict the effect we&#39;re having on our Earth, and the long-term trend. They then advice policy makers on the best path to take.
</p>

