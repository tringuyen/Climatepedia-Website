<a name="job"></a>
<p>
You&#39;ve heard enough, and want to know what you&#39;ll really be doing on the job as a Climatologist? Read on!
</p>
<p>
	Depending on your specific position, you may end up either running computer models in your office, or conducting research from the ground or aircraft. 
</p>
<p>
	<em>Entry-level:</em>Many climatologists in the Federal Government start with an intern position where they may do routine data aggregation, calculations, analysis, or simple forecasting for training and experience. “Entry-level” at a university essentially entails being a teaching assistant for research professors, or assisting with their climate research. At a private consulting firm you’ll often start as an intern, and upon receiving a full-time offer you’ll work on individual accounts. 
</p>
<p>
	<em>Mid-level:</em> The path diverges as you gain experience in your specialty. Working for the Federal Government you may be assigned to a permanent station or be a part of a research and development team influencing federal policy decisions. At a consulting firm you may manage individual accounts or more junior consultants. At a corporation you may advance to a senior position.
</p>
<p>
<em>Top-level:</em> Experienced climatologists can advance to managerial or supervisory positions with more administrative tasks, or lead the research in a certain topic. The field is rapidly changing though, and the opportunities for you at this level of expertise are limitless.
</p>

