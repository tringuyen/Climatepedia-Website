<h4>Employment Preparation</h4>

<a name="experience"></a>
<h5>Getting Experience and Volunteering as a Climatologist</h5>

<p>
	Volunteer! On the path to becoming a climatologist this is one of the best ways to first get your foot in the door, and shows that you have the initiative and passion to get out there yourself and learn. Here are some positions that may be interesting, and many more are available at your local weather stations. [1]
</p>
<ul>
<li>
	Nearly every state has its own weather administration and are all eager for volunteers that have an interest in becoming a climatologist.
</li>
<li>
	As a storm spotter for the National Oceanic and Atmospheric Administration (NOAA).
</li>
<li>
	As a Daily Weather Observer with the Cooperative Observer Program (COOP).
</li>
<li>
	If you’re a seafaring individual, then as a Ship Weather Observer with the NOAA.
</li>
<li>
	As a Volunteer Weather Spotter with the National Weather Service.
</li>
<li>
	As a volunteer at the Weather Volunteer Observers Network of the National Hurricane Center (ON-NHC).
</li>
<li>
	Set up a local weather station and provide information to your local radio station with a daily report - an amateur climatologist!
</li>
<li>
	Get experience at the local airport, television or radio station as a weather data compiler or weather statistics researcher.
</li>
<li>
	Learn to read and interpret the various types of weather maps, charts, and data available to climatologists.
</li>
<li>
	Learn how to use database software. Many databases operate on the same type of architecture and have similar commands, but it would be wise to contact a climatologist at a local university and see what database software they use.
</li>
</ul>

<a name="internships"></a>
<h5>Climatologist Internships</h5>

<p>Internships are one of, if not the, best way to get experience in the Climatology field of work before graduating from college, and the same is true of nearly any industry. Your best bet is to find the climate organization that is closest to you and see if they either offer an intern/ co-op program, or may be willing to take you on as a volunteer.

For example, the Climate Institute offers internships for future climatologists in its Washington, DC-based headquarters.</p>

