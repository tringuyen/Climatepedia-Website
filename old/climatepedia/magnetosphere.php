<h5>What is a Magnetosphere?</h5>
<p>
The magnetosphere is the area around a planet in which that planet’s magnetic force is the dominant pressure. It is actually shaped by the solar wind, or the flow of ionized plasma constantly emitted from the Sun, and is what protects life on Earth from most of the harmful effects of solar activity. Other planets that have intrinsic magnetic fields also have magnetospheres.
</p>

<a name="importance"></a>
<h4>Importance and Effect of the Earth's Magnetosphere</h4>
<p>
The magnetosphere has allowed for life to flourish on Earth, as without it the supersonic, extremely hot, solar wind would constantly be blasting into the surface of the Earth. It also protects us from the brunt of most other solar activity, like coronal mass ejections (CME) or solar flares. Even with the magnetosphere though, large CMEs or solar flares can be extremely damaging to equipment on the Earth’s surface, not to mention in orbit. For example, in the largest geomagnetic storm ever recorded, the Carrington Event of 1859, a CME caused by a solar flare caused telegraph systems across North America and Europe to fail. If an event of this magnitude happened today the damage to electrical equipment would be disastrous. 
</p>

<a name="structure"></a>
<h4>Structure</h4>
<p>
The structure of the magnetosphere is determined by the flow of the solar wind and the electric currents that run inside of it. Broadly, it is compressed on the sunward side by the solar wind’s dynamic pressure, and is swept back on the night side by the solar wind flowing past the Earth. The composition of different areas within the magnetosphere depends on the movement of plasma as it interacts with various electric, magnetic, and dynamic forces. Each of the major sections of the magnetosphere is discussed below, and you can view the diagram halfway through the section for a visual representation of them.
</p>

<a name="bow"></a>
<h5>Bow Shock</h5>
<p>
The bow shock is the point at which the Sun’s supersonic solar wind flow is forced to slow down before it hits the magnetosphere. The physics behind this process is similar to sonic booms that jets when they exceed the sound speed of the medium they’re travelling in (air). The bow shock changes distance from the Earth depending on the strength of the solar wind flow. As plasma crosses the bow shock its density, temperature, and magnetic field increase, while its velocity decreases.
</p>

<a name="sheath"></a>
<h5>Magnetosheath</h5>
<p>
The magnetosheath is the region between the bow shock and the magnetopause, and contains compressed and heated solar wind plasma.
</p>

<a name="pause"></a>
<h5>Magnetopause</h5>
<p>
The magnetopause is the boundary at which the magnetosphere ends on the sun-ward side of the Earth, and where the solar wind actually interacts with the magnetosphere’s magnetic pressure. Most of the solar wind particles that hit the magnetopause are deflected and swept back into the magnetotail, while the ones that penetrate become part of the energetic Van Allen radiation belts.
</p>

<a name="tail"></a>
<h5>Magnetotail</h5>
<p>
The magnetotail is the area on the night side of the Earth where the solar wind carries the magnetosphere a long distance back into space (upwards of 3,000 Earth radii). This area acts as a reservoir for plasma and energy, which is released into the inner magnetosphere at various intervals during magnetospheric substorms.
</p>
<ul class="list">
<li><strong>Equatorial Current Sheet</strong>: This separates the magnetotail into two regions called the lobes, and flows from dawn to dusk.</li>
<li><strong>Lobes</strong>: The magnetic field in the north (south) lobe is directed away form (toward) the Earth. Plasma density is very low in this area.</li>
<li><strong>Plasmasheet</strong>: The plasma sheet is found between the lobes in the magnetotail, and contains extremely hot ions and electrons (estimated around 3keV and 1keV respectively).</li>
</ul>

