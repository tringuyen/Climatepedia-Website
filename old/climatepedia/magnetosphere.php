<a name="space"></a>
<h4>Space Weather and the Magnetosphere</h4>
<p>
Space weather is the interaction of the solar wind and solar events with the Earth, and is a huge concern for people both in space and on the ground. Solar events can not only damage sensitive spacecraft that power our GPS systems, television networks, credit card networks, etc., and harm astronauts in space, but they can also induce electric fields on the surface of the Earth – burning out electrical components in large pipelines like those used to transport oil or electricity.
</p>

<h5>Substorms</h5>
<p>
Substorms occur when the solar wind’s interplanetary magnetic field points southward for a brief time (30-60 mins), allowing for reconnection to occur. Many different hypotheses about substorms have been suggested, and we chose a common one to detail the events. Substorms start with a growth phase, during which energy extracted from the solar wind is stored in the magnetosphere. The next phase is the expansion phase, during which this stored energy is released and unloaded. Finally, the recovery phase returns the system to normal.
</p>

<h5>Magnetic Storms</h5>
<p>
Magnetic storms are large, prolonged disturbances of the magnetosphere caused by variations in the solar wind, and many follow coronal mass ejections or are caused by high speed streams of the solar wind. Furthermore, magnetic storms occur on a global scale, whereas substorms are more local to the magnetotail.
</p>
<p>
In one suggested train of events, magnetic storms begin with a phase called sudden commencement, during which a pressure increase from an interplanetary disturbance compresses the magnetosphere. The main phase of the magnetic storm occurs when the southward component of the IMF causes magnetic reconnection at the magnetopause, which increases ring current injection with energetic solar wind plasma. The recovery phase is last, and occurs as the southward IMF component weakens, slowing the reconnection rate, and reducing the injection of particles into the ring current.
</p>

<a name="other"></a>
<h4>Other Planets' Magnetospheres</h4>
<p>Scientists have been able to observe the magnetic activity surrounding most of the planets in our solar system.</p>
<ul>
<li><strong>Mercury</strong> – Mercury has a magnetosphere, but it is incredibly small compared to the rest of the solar system’s.</li>
<li><strong>Venus</strong> – Earth’s sister planet, Venus, does not have a magnetosphere in the same sense as the Earth and some of the other planets. This is because its magnetic field is not intrinsic, but is induced by the solar wind, and thus has no interaction with the solar wind.</li>
<li><strong>Mars</strong> – Mars does not have a magnetosphere, but is comparable to Venus in that its magnetic field is induced.</li>
<li><strong>Jupiter</strong> – Jupiter does have a magnetosphere, and it is the largest of all the planets. Jupiter’s moon Ganymede also has a magnetosphere.</li>
<li><strong>Saturn</strong> – Saturn does have a magnetosphere, and it is unique in that it is not tilted in respect to its axis.</li>
<li><strong>Uranus</strong> – Uranus does have a magnetosphere, and it is very similar to the Earth’s.</li>
<li><strong>Neptune</strong> – Neptune does have a magnetosphere, and it too is very similar to the Earth’s.</li>
<li><strong>Pluto</strong> – Is no longer considered a planet, so it doesn’t belong in this list and you’ll be left wondering whether or not it has a magnetosphere. Just kidding, we actually don’t know if Pluto has a magnetosphere or not. Furthermore, the probe that was sent out past Pluto to take measurements (New Horizons) does not have a magnetometer, so we won’t know any time soon either.</li>
</ul>


