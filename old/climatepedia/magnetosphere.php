<a name="plasma"></a>
<h5>Plasmasphere</h5>
<p>
The plasmasphere is the area around the Earth where cold plasma co-rotates with the Earth due to being dominated by the geomagnetic field. It is located above the ionosphere, and its outer boundary is the plasmapause.
</p>

<a name="van"></a>
<h5>Van Allen Radiation Belt</h5>
<p>
The Van Allen radiation belts consist of energetic particles on trapped orbits, and there are both electron and proton radiation belts. The energetic particles come from cosmic rays and are kept in orbit by Earth’s magnetic field. The radiation belts are particularly dangerous for space missions beyond low earth orbit, as the spacecraft and/or astronauts must cross through the Van Allen radiation belts, and are thus subject to Van Allen particle radiation. The radiation in this area from the energetic particles, solar flares, and cosmic rays are particularly damaging to electrical equipment on satellites, and usually needs to be shielded against.
</p>

<a name="ring"></a>
<h5>Ring Current</h5>
<p>
The ring current is the area in which large amounts of energy are placed during magnetic storms. By measuring the Ring Current with the disturbance storm time (Dst) index, we can measure the strength of a magnetic storm.
</p>

<a name="reconnection"></a>
<h4>Reconnection</h4>
<p>
Reconnection is one of the most fascinating discoveries of modern science, and has been suggested to be one of the primary means by which massive amounts of energy are transferred in our universe (on the scale of the energy transferred between the Sun and planets). To narrow it down to the scope of this article, reconnection is thought to drive magnetospheric flows and the dynamics of the solar corona.
</p>
<p>
When a magnetized plasma parcel (a flux tube) with one polarity is brought up to a magnetized plasma parcel with the opposite polarity, magnetic reconnection is thought to occur. At this point field lines connect and change their connectedness (topologies). This process of reconnection allows previously unconnected regions to exchange plasma, and hence mass, energy and momentum. This allows solar wind energy to be directly coupled with and channeled into the magnetosphere.
</p>
<p>
The first point of reconnection in the Earth-Sun system occurs at the magnetopause, where the solar wind (carrying the interplanetary magnetic field – IMF), interacts with the Earth’s magnetic field. The Earth always has a northward topology due to its dipole configuration, but the solar wind switches between southward and northward, and so this reconnection only happens when the solar wind has a southward component (opposite of the Earth’s).
</p>
<p>
A second reconnection point occurs in the magnetotail at the neutral point, which allows plasma to flow from the magnetospheric currents back into the solar wind and away from the Earth, re-establishing equilibrium of the system.
</p>

<a name="earth"></a>
<h4>Characteristics of the Earth's Magnetic Field</h4>
<p>
The Earth’s intrinsic magnetic field is most likely caused by electric currents in its liquid core, and sustained by the interaction between these electric currents and the existing magnetic field, which is described in the Dynamo Theory. The magnetic field takes on a dipole shape, where magnetic lines of force point out of the South Pole and back into the North Pole.
</p>

<a name="sun"></a>
<h4>Magnetosphere Interaction with the Sun</h4>
<p>
The magnetosphere is the primary point of connection between the Sun and the Earth, and is what drives most space weather events. The solar wind that is constantly ejected from the Sun at a supersonic speed consists of charged ions, which carry the Sun’s interplanetary magnetic field (IMF) out into space with the solar wind flow. 
</p>
<p>
A shock wave called the bow shock is created on the side where the solar wind plasma collides with the magnetosphere, as the solar wind is supersonic and the magnetosphere acts as an obstacle to its flow. This shock is similar in concept to a sonic boom. In this way the solar wind is directly responsible for shaping the magnetosphere, as a stronger solar wind will push the magnetopause back, and a weak solar wind will allow the magnetopause to grow. This pressure of balances is essentially found by balancing the solar wind’s dynamic pressure with the magnetosphere’s magnetic pressure.
</p>
<p>
This interaction results in a magnetospheric<img src="css/images/pedia/solarwind.jpg" alt="the sun's solar wind interaction with the earth's magnetic field and magnetosphere" width="350" height="255" class="wrapright" align="right"> cavity that has a long tail consisting of two antiparallel bundles of magnetic field (the magnetotail) that stretch in the antisolar direction for an unknown distance, though it has been estimated to be greater than 3,000 Earth radii.
</p>
<p>
When the IMF and Earth’s magnetic field connect it results in the transfer of energy from the solar wind to the magnetosphere, and by extension from the Sun’s atmosphere to the Earth’s atmosphere. As described above, this energy is transferred in a process called reconnection, which occurs primarily at the point where a solar wind stream with a southward magnetic flux meets the Earth’s magnetosphere, which has a northward magnetic field at that point. Upon connecting, ionized plasma flows into the Earth’s magnetic currents and can build up at certain points – ultimately causing sub storms and magnetic storms. A second primary reconnection point on the dark side of the Earth reverses the flow of ionized plasma, returning it into the solar wind to flow past the Earth.
</p>
<p>
The Earth’s dipole moment is also tilted with respect to the axis, which means that the magnetic North Pole is not actually directly aligned with the geographic North Pole, and same for the South Pole. It is also constantly moving, so each time it has been measured it was further northwestward from its last measured location.
</p>

<a name="climate"></a>
<h4>Magnetosphere Interaction with the Earth's Climate</h4>
<p>
This part of the magnetosphere’s function is one of the least explored, and is being researched by today’s scientists.
</p>
<p>
At this point most believe that energy transferred from the solar wind through to the lower levels of the Earth’s atmosphere have only a minor affect on the climate, though very little research has been done to deny or confirm this belief.
</p>
<p>
While changing electric fields in the atmosphere can have catastrophic effects on our equipment both on the ground and in the air, it looks like global warming is most likely not linked to the magnetosphere.
</p>

<a name="aurora"></a>
<h4>Aurora Borealis (Northern Lights)</h4>
<p>
Aurora Borealis, or the Northern Lights, are an event that occurs when photons in the Earth’s upper atmosphere are excited (ionized). The ionization occurs when solar wind plasma has been injected into the Earth’s magnetosphere and been carried down magnetic field lines into the Earth’s upper atmosphere. Large auroral disturbances occur during magnetic storms, when huge amounts of energy course through the magnetosphere.
</p>

