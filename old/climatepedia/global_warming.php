<h1>Global Warming</h1>


<p>The term global cooling describes the 1970s belief that tiny air pollutants (aerosols) would decrease the earths average temperature-- some even thought that the earth could enter another ice age. Observations and new developments in climate science over the last forty years, however, have shown that global cooling predictions were inaccurate. </p>
<p>In the 1970s, some scientists incorrectly predicted that the cooling effect of aerosols would outweigh the warming effect of greenhouse gases. While aerosols do have a cooling effect, the earth is generally trapping more energy as a result of the greenhouse effect. </p>
<p>Even in the 1970s, global cooling was not a consensus view, but rather was only one perspective of climate scientists. For example, an American Meteorological Service literature review of papers between 1964 and 1979 found that more papers had actually predicted that the earth would experience warming than cooling. Nevertheless, global cooling was popularized in the 1970s through news outlets such as Newsweek, Times, and New York Times. </p> 



