<a name="electricity"></a>
<h4>Electricity</h4>
<p>
Geothermal energy is a considered a renewable source of power because the heat used to generate electricity is continually produced within the Earth’s core. Power plants that use geothermal energy as fuel require extremely hot water or steam (300 to 700 degrees Fahrenheit) [2]
</p>
<p>
The type of conversion used to turn geothermal energy into electricity depends on whether the fluid used is steam or water, and how hot it is. The decision points are whether the source of water is liquid or gas, and whether it is above or below moderate-temperature (around 360-400 degrees).
</p>

<a name="dry"></a>
<h5>Dry Steam Power Plants</h5>
<p>
The first geothermal power plants used dry steam systems, where they used steam erupting from the ground and routed it to rotate turbines, generating electricity. The first power plant of this kind was located in Lardarello, Italy in 1904, and is still in use. These power plants largely eliminate the usual impact on the environment that power plants have, as the actual energy generation process produces no emissions and requires no fossil fuels, and there are no storage or transportation-related emissions. For this reason, proponents of the fight against global warming point to steam-based geothermal energy as one of the cleanest sources of alternative energy.
</p>


<a name="flash"></a>
<h5>Flash Steam Power Plants</h5>
<p>
Flash steam plants require liquid water above 360 degrees Fahrenheit, and are the most common types of geothermal power plants operated today. In these types of systems hot liquid water is sprayed into a low-pressure tank, causing it to quickly vaporize into gaseous form, and this vapor is what is used to turn the energy-generating turbine.
</p>

<a name="binary"></a>
<h5>Binary Cycle Power Plants</h5>
<p>
Binary-Cycle Power Plants are used in geothermal areas that contain water of temperatures below 400 degrees Fahrenheit. <img src="css/images/pedia/geothermal.jpg" alt="diagram of the process at a binary geothermal power plant" class="wrapright" align="right"> Central to this type of system is a heat exchanger, where the hot geothermal fluid and a secondary (“binary”) fluid source with a lower boiling point than water interact. The hot geothermal fluid transfers heat to the binary fluid, causing it to rapidly vaporize, turning the turbines.
</p>
<p>
The U.S. Department of Energy states that the resource that binary cycle power plants use, “moderate-temperature (less than 400 degrees F) water, is the most common type of geothermal resource and most geothermal power plants in the future will be binary cycle-plants.” [1]
</p>
<p>
As per above, this flash-vaporizing technique emits no dangerous compounds into the atmosphere, thus making it a favorite of those fighting against global warming.
</p>


<a name="heating"></a>
<h5>Geothermal Energy Used in Heating</h5>
<p>
Much of the Earth’s geothermal energy is used directly for district heating systems and in geothermal heat pumps, thereby producing hot water and controlling building temperatures. The direct use has been recorded since the times of the ancient Romans, who used hot springs for bathing, cooking and heating.
</p>
<p>
Cities like Reykjavik, Iceland use district heating systems to pump hot water from beneath the Earth’s surface to flow through buildings for heat; 95% of Reykjavik’s buildings obtain heat in this way [2].
</p>
<p>
Canada has also had success with using geothermal energy for heating and cooling buildings. The Canadian Centre for Energy states that there are about 30,000 ground-source heating pumps that utilize the temperature difference between the surface of the earth and meter or two beneath the surface. There are also about 5,000 systems in Canada that cool and heat office buildings, universities, industrial parks, prisons, and curling rinks. [3]
</p>
<p>
Canada has also been using a technology called the Underground Thermal Energy Storage system since the 1980s, which stores heat during the summer in order to later use it in the winter. The largest facilities that use this type of system are the municipal center in Scarborough, Ontario, and the Carleton University in Ottowa. [3]
</p>
<p>
Geothermal energy is also used industrially to dehydrate vegetable and fruit products.
</p>


<a name="history"></a>
<h4>History of Geothermal Energy</h4>
<p>
The roots of geothermal energy use can be traced back thousands of years to ancient civilizations using hot springs for bathing, recreation and heat; some cultures also saw hot springs as spiritual sources of healing. The modern history is arranged by date below:
</p>
<ul class="list">

<li> 1700’s: Pisa, Italy geothermal energy used to extract boric acid from the Laderello Fields using steam. </li>

<img src="css/images/pedia/geyser.jpg" alt="a geyser exploding as an example of a geothermal energy source" class="wrapleft" align="left" width="250" height="375">

<li>1830: First time geothermal energy is known to be commercialized in the United States, where Asa Thompson charged $1 for a bath in one of her hot springs.</li>

<li>1864: Oregon, USA hotel heats rooms using energy from underground hot springs.</li>

<li>1904: Once again in the Larderello Fields, for the first time steam was used to generate electricity.</li>

<li>1911: The world’s first geothermal power plant in the Larderello Fields, which was the only industrial producer of power from a geothermal source for the next 47 years.</li>

<li>1922: United States of America’s first geothermal energy power source built by John D. Grant at a hotel in California; it was used to power the hotel.</li>

<li>1958: World’s second industrial geothermal power plant built in New Zealand.</li>

<li>1960’s: First large-scale geothermal power plant constructed in the United States.</li>

</ul>

<a name="production"></a>
<h4>Production of Geothermal Energy</h4>
<p>
Geothermal energy finds its way from reservoirs to the surface of the Earth as geysers, volcanoes, hot springs, and fumaroles (volcanic gas releases). This activity mostly occurs around the edges of tectonic plates, which is also where volcanoes and earthquakes are most common; the largest single area of such activity is called the Ring of Fire around the Pacific Ocean.
</p>
<p>
Over the past 10 years geothermal energy has seen a stable 1.2% annual global growth rate.
</p>
