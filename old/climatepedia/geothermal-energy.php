<a name="overview"></a>

<p>
Geothermal Energy is the term for thermal energy that comes from the Earth. The Earth’s thermal energy comes from a variety of sources, like energy that has been absorbed by the sun’s rays, from the original formation of the planet, continuous radioactive decay of minerals, and from volcanic activity.
</p>
<p>
It is a unique alternative energy source in that unlike solar or wind energies, it can provide energy 24-hours a day, 365 days a year. It is also extremely emissions-efficient, and is thus often supported as a means to mitigate anthropogenic climate change in debates about global warming.
</p>
<p>
About 10,715 megawatts of geothermal power is online worldwide, which is in addition to 28 gigawatts of geothermal energy that is used directly for heating, spas, industrial procedures, desalination and agricultural processes [International Geothermal Association].
</p>
<p>
Geothermal energy has been an effective solution to heating and energy needs for thousands of years, but is largely restricted to areas near tectonic plate boundaries, as they are where the majority of the Earth’s energy is released.
</p>
