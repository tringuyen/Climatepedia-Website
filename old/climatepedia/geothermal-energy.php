<a href="effect"></a>
<h4>Effect on Climate Change</h4>
<p>
When used as direct heating, geothermal energy has essentially no adverse impact on the environment or the climate.
</p>
<p>
Geothermal power plants do not use fuel to generate electricity, as they get their heat directly from the Earth <a href="#electricity" >(see technology section)</a>, so their emissions are extremely low. According to the Energy Information Association of the United States, their carbon dioxide emissions are less than one percent of the CO2 emissions of a power plant that burns fossil fuels to produce electricity.
</p>
<p>
Thus, geothermal is one of the most emission-efficient alternative sources of fuel, and is often cited as an excellent alternative in the context of global warming debates.
</p>

<a name="economics"></a>
<h4>Economics of Geothermal Energy</h4>
<p>
As with most of the other types of renewable energy, geothermal energy’s economic advantage is that once the initial investment has been made, the energy is free. Of course, this is extremely simplified as real plants may have to pay regulatory fees for permits, and will have to pay for their staff and maintenance.
</p>
<p>
As of now the costs are prohibitively high for most commercial and private usage. Certain areas that are uniquely positioned over strong sources of geothermal energy, like Canada and Iceland, have had more success in widespread geothermal energy use.
</p>



