<a name="effect"></a>
<h4>Effect on the Climate & Environment</h4>
<p>
Nuclear energy power plants themselves do not directly emit the type of fossil fuel emissions related to anthropogenic climate change like carbon dioxide, nitrogen oxides, or sulfur dioxide. In the holistic nuclear energy production process though, the current techniques used to mine, enrich, and transport uranium (the fuel behind nuclear energy) do have associated emission costs that impact our atmosphere and may contribute to global warming.
</p>
<p>
It does however fare better than biofuels, wind farms, or photovoltaics in term of the amount of space the process requires, as a nuclear power plant occupies relatively small tracts of land. However, storage of nuclear waste is a pressing issue that no clear answer exists for; currently, most nuclear waste is stored at the production facility in thick concrete storage facilities.
</p>



<a name="renewable"></a>
<h4>Is It Renewable Energy?</h4>

<p>
Nuclear energy produced through fission is not renewable, as it uses uranium as fuel. Uranium is a relatively rare metal found in rocks all around the Earth, while it is about 100 times more common than silver [3], it is still of a limited supply. Most is mined in the United States, and the specific type of Uranium used in nuclear fission, U-235, must be extracted before use.
</p>

<a name="safety"></a>
<h4>Nuclear Power Safety Concerns</h4>
<p>
The Nuclear Regulatory Commission (NRC) regulates the U.S. Nuclear Power industry, and strives to ensure the safety of those who work in or live near nuclear power plants. 
</p>
<p>
The most commonly cited threats that nuclear power plants pose to the populace are in the risk of a plant becoming unstable, like the 3 mile island or Chernobyl incidents, and in the cumulative effect of nuclear waste. The harm of merely living near a nuclear power plant is negligible, as the amount of radiation one will absorb due to their proximity to the nuclear material is less than one diagnostic X-ray procedure.
Modern nuclear energy production facilities are equipped with sophisticated automatic safety features, which are designed to shut down the power plant before its safety margins are exceeded. The industry refers to unplanned automatic shut downs of this nature as “scrams” or “trips”.
</p>
<p>
The high-level nuclear waste of a production facility consists of the spent fuel used in the nuclear reactor, and can retain its radioactivity for thousands of years. <img src="css/images/pedia/nuclearwaste.jpg" alt="nuclear energy waste is of great concern to the public" class="wrapleft" width="300" height="299" align="left">Currently most nuclear waste is stored either in on-site locations or special external storage facilities. The U.S. Department of Energy has proposed development of  a permanent waste disposal site at Mt. Yucca, Nevada, where waste could be stored for thousands of years.
</p>
<p>
All nuclear power plants are equipped with heavily armed security forces to keep intruders out.
On a national level, proponents of nuclear energy point to it as being key to national security for any country. It is relatively unaffected by variable weather, unpredictable cost fluctuations, or foreign suppliers. However this last point is questionable, as in 2008 only 14% of Uranium delivered to U.S. nuclear power plants was mined in the U.S., while the rest was from foreign countries. Australia and Canada supplied 42%, Uzbekistan, Russia, and Kazakhstan provided 33%, and the United Kingdom, Czech Republic, South Africa, Namibia, Niger, and Brazil delivered 11%. [3]
</p>



