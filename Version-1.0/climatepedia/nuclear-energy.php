<a name="history"></a>
<h4>History of Nuclear Power</h4>
<a name="first"></a>
<p>
Nuclear research initially began during World War II as focused mainly on creating nuclear weapons to quickly end a war that was shaking the foundation of our civilizations, but afterwards scientists turned towards using their new-found source of power for peaceful means. Since then a wide array of industries and fields have found uses for nuclear power, from medical to industrial applications.
</p>
<p>
Ancient Greek philosophers were the first to develop the concepts that underlay nuclear reactions: that all matter consists of tiny building blocks called atoms, and by 1900 physicists had ascertained that these little atoms contained incredible amounts of energy. In 1904 the father of nuclear science, Ernest Rutherford, wrote that “If it were ever possible to control at will the rate of disintegration of the radio elements, and enormous amount of energy could be obtained from a small amount of matter.” Shortly thereafter, Albert Einstein came out with his famous equation: E=mc2, which defines the relationship between energy, mass, and the speed of light.
</p>
<p>
In the early-mid 1900’s the scientists Enrico Fermi, Otto Hahn, Fritz Strassman, and Lise Meitner were all involved in various experiments in splitting many kinds of atoms. Eventually Lise Meitner realized that the masses of the light elements left over from splitting a uranium atom did not equal the mass of the uranium atom, and used Einsteins theory to demonstrate that the lost mass had turned into energy, thus proving Einstein’s work.
</p>
<p>
On December 2, 1942, Enrico Fermi led a group of scientists in creating the first self-sustaining chain reaction beneath the University of Chicago’s athletic stadium, thus ushering in the nuclear age. From this point forward most work regarding nuclear power was under the umbrella of the Manhattan Project, the aim of which was to develop a weapon to be used in World War II. <img src="css/images/pedia/nuclearreactor.jpg" class="wrapright" width="371" height="322" align="right" alt="nuclear power plant history" > After the war ended though, the United States government encouraged the development of nuclear energy for peaceful uses, and the first energy generated by nuclear reactor was created on December 20, 1951 in Idaho.
</p>
<a name="commercial"></a>
<p>
Around this time the major goal was to prove that nuclear power could be commercially viable for large-scale energy production, and the first commercial power plant was located in Shippingport, Pennsylvania. During the 1960s the nuclear power industry grew rapidly, but during the 1970s and 1980s growth slowed as safety issues and environmental concerns increased. However, by 1991 the United States had one-fourth of the world’s operating nuclear power plants, double any other country, and produced 22 percent of its energy using it.
</p>
<a name="now"></a>
<p>
Nuclear power plant construction stalled at this point in the U.S., with the last reactor being built in 1996. Other countries, like France and Japan, have continued to push forward. The recent economic trend of globalization has seen a resurgence of interest in nuclear power among developing nations, with China leading the way. If China builds all 160 of the nuclear power plants it has planned or proposed it will have more than any other nation, including the United States. Industrialized countries too have seen a recent resurgence of interest in nuclear energy as concerns of global warming push societies to adopt sources of energy with less greenhouse gas emission.
</p>




<a name="technology"></a>
<h4>Technology Behind Nuclear Power</h4>

<p>
A nuclear power plant harnesses the energy from nuclear reactions then uses it to heat water into pressurized steam, and uses this steam to drive a turbine generator that produces electricity. This is the same way most power plants work, in that they use varying fuel sources to create steam and generate electricity. At present, nuclear plants rely on breaking apart a uranium atom using a process called nuclear fission. The opposite, combining two atoms, is called fusion and has been heavily researched for decades because of its greater potential for generating energy. However, no controllable option for this type of reaction has yet been found. The steam that is produced by the nuclear reactor is cooled back into water after the process so that it can be reused. The buildings in which the reactions take place are the containment buildings, and have concrete walls 3 to 6 feet thick to contain any rogue radioactivity [2].
</p>
<p>
The Uranium fuel that is used is kept in small ceramic pellets about the size of an adult’s fingertip, which are stacked in a row along 12-foot metal fuel rods called fuel assemblies. Each of these ceramic pellets contains the energy equivalent of about 150 gallons of oil [3].
</p>
<a name="pressurized"></a>
<h5>Pressurized Reactors</h5>
<p>
These reactors use two sources of water for steam production – one that becomes irradiated and one from clean sources like lakes and rivers. The source that is reused within the facility is heated up by the nuclear reactions but kept from boiling, and thus stays liquid. It then flows through a second piece of equipment called a steam generator, which itself is heated up, and in turn boils the second source of water to produce steam. The irradiated water is recycled back to the reactor where the process is repeated.
</p>

<a name="boiling"></a>
<h5>Boiling Water Reactors</h5>
<p>
In these types of facilities the water is simply boiled, and thus turned into steam that turns the turbine generators.
</p>

<a name="fission"></a>
<h5>Nuclear Fission</h5>
<p>
There is a huge amount of energy stored in the bonds of an atom, which fission breaks apart in order to harness. During this process an extremely small particle called a neutron is fired at a uranium atom, breaking it apart and releasing heat and radiation in the process. More neutrons are released from the split uranium atom, which go on to hit other uranium atoms, resulting in a chain reaction.
</p>

<a name="fusion"></a>
<h5>Nuclear Fusion</h5>
<p>
Nuclear fusion produces energy as a byproduct of combining two atoms, and is the primary way in which our sun creates its energy. 
</p>


<a name="economics"></a>
<h4>Economics of Nuclear Power</h4>
<p>
Primary factors in the economics of nuclear power include: the demand/supply of uranium, the cost of other types of fuel, the initial capital investment in a nuclear power plant, and political or social influences on energy production - like safety and environmental concerns.
</p>
<p>
First and most evidently, as the cost of oil per barrel increases the attractiveness of nuclear energy, as well as most other alternative energy sources, also increases.
</p>
<p>
The demand for uranium is expected to greatly outstrip the supply of it over the next 10 years as the developing world’s nuclear power plants start to operate. Countries like China and India have been investing large amounts in their nuclear energy industries, while the mining of uranium is not expected to match this growth rate.
</p>
<p>
Interest in nuclear energy has been revived lately as anthropogenic climate change has become more accepted by the world. Increased fears of global warming lead to greater interest in non-carbon-based energy sources, such as nuclear energy. National commitments to highly energy-efficient and low greenhouse-gas-emitting sources of energy to stem the tide of global warming will likely result in increasingly more nuclear energy capital investment over the coming decade. As corporations are increasingly face their contribution to global warming they are also likely to turn to alternative energy sources like nuclear energy, thus further increasing its demand.
</p>
<p>
The events that could most seriously impact the nuclear energy markets are a nuclear accident in one of the world nuclear power plants, an explosion of a nuclear device or nuclear plant sabotage by terrorist groups, or the world’s inability to address both non-proliferation goals and nuclear energy advancement.
</p>

