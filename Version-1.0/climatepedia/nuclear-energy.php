<a name="overview"></a>
<p>
Nuclear power is created through controlled nuclear reactions that release heat,  which is then used to boil water, releasing steam that used to spin a turbine. As the turbine spins its generator turns and the magnetic field changes, creating electricity. All nuclear reactors today utilize nuclear fission, or the breaking apart of a uranium nuclei to release energy; nuclear physicists hope to some day harness the power of nuclear fusion, or the fusion of two nuclei, to generate energy on a much more massive scale. Interest has recently increased in nuclear energy as an alternative source of energy as global warming concerns take center stage around the world.
</p>


