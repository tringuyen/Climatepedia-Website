<a name="uses"></a>
<h4>Nuclear Power Uses</h4>
<p>
Nuclear energy’s biggest use is as electricity generated by power plants, but it has also found a niche in the propulsion systems of naval vessels and in equipment used in space.
</p>



<a name="electricity"></a>
<h5>Electricity</h5>
<p>
	Industrialized nations like the U.S., U.K., Germany, France, and Japan account for most of the electricity produced by nuclear power plants, but developing countries like China and South Africa have been constructing new facilities; currently 31 nations produce their own nuclear power (link to countries section). Further below is a table listing all nuclear power-producing nations (and ones planning to do so), as well as the number of reactors they operate and the percentage of their energy that comes from nuclear power. 
</p>

<a name="naval"></a>
<h4>Naval Propulsion</h4>

<p>
According to the World Nuclear Association, greater than 180 small nuclear reactors generate energy for around 140 ships, most which are submarines but include other types - from icebreakers and aircraft carriers. <img src="css/images/pedia/submarine.jpg" class="wrapleft" alt="nuclear generators are used in military submarines" width="300" height="233" align="left">This type of power source is best suited for ships that have to be away from port for prolonged periods of time, and becomes more practical as fossil fuel sources become more limited. It has also proven particularly useful on icebreakers in the Russian arctic, where large amounts of energy are needed to break the unusually thick ice and refueling is difficult.
Nuclear propulsion systems use highly-enriched uranium to produce large amounts of energy while taking up relatively small amounts of space, and refueling is only needed after 10-50 years depending on how new the design of the core is.
</p>
<p>
In 2009 China’s COSCO, the world’s largest shipping conglomerate, expressed interest in putting nuclear propulsion systems on merchant ships in order to reduce the emissions that commercial shipping currently releases.
</p>



<a name="space"></a>
<h5>Nuclear Power in Space</h5>
<p>
Unmanned space vehicles also rely on nuclear power for their energy, though not through fission reactions. Instead they utilize radioisotope thermoelectric generators (RTGs) to generate power that is used for running their computers. Most RTGs produce around 300 watts of electricity and can do so for many years, demonstrated by the scacecraft Voyager1 and Voyager 2 that were launched more than 30 years ago but are still operational billions of miles from the sun.
</p>
<p>
Not all space missions depend on this power source, but many do. Two of the most well-known missions that have utilized RTG power are the Galileo mission and the New Horizons mission, which were intended to study Jupiter and Pluto, respectively. The Galileo spacecraft mission orbited Jupiter 35 times during a 2-year timespan, and scientists used the data it collected in an effort to better understand the Earth’s atmosphere and climate change. The New Horizons spacecraft mission was the latest NASA mission, and is in transit to Pluto at this moment. [4]
</p>


