<a name="alternative"></a>
<h4>Alternative Energy v.s. Renewable Energy</h4>
<p>
Alternative energy and renewable energy are terms often interchanged in daily conversation, and indeed in meaning they are both viable in most situations. As mentioned earlier, renewable refers to any natural source that is naturally replenished, while alternative usually refers to any source other than the traditional fossil fuels. This becomes important when talking about nuclear energy, as it can be considered an alternative source of energy to fossil fuels, but many do not consider it renewable. The debate on this is addressed in the Nuclear Energy section below.
</p>

<a name="importance"></a>
<h4>Why Is It Important?</h4>

<a name="sustainability"></a>
<h5>Sustainability</h5>
<p>It is evident that eventually our sources of oil and coal will be depleted, at which point we’ll need to have already developed an infrastructure of energy production capable of supporting our world, or else we’re going to be in serious trouble. The actual time frame for that point is highly debated and has been revised many times over the years.</p>

<a name="security"></a>
<h5>Energy Security</h5>
<p>Producing energy within a nation’s own borders decreases dependence on others, increasing security in the event of a cutoff of supply.</p>

<a name="environment"></a>
<h5>Environment</h5>
<p>Burning fossil fuels increases the toxic particles in the air, speeding up anthropogenic climate change, or global warming.</p>

<a name="benefits"></a>
<h4>What Are The Benefits?</h4>

<a name="environmental"></a>
<h5>Environmental Benefits</h5>
<p>Renewable energy sources have a much lower environmental impact than conventional energy technologies. Sources such as fossil fuels contribute heavily to issues like smog, acid rain, and global warming.</p>

<a name="jobs"></a>
<h5>Jobs and Economy</h5>
<p>The majority of capital investment in renewable energy goes into the materials and labor to build and maintain the facilities, as compared to the large sums spent on importing another nation’s oil. In the United States renewable energy investments are frequently spent in the same state, and many times even in the same town. This means that the investment dollars are spent stimulating the local economy. Of course, oil companies also employ thousands of people, and are currently able to provide energy at a lower price than renewable sources because the infrastructure is stronger. While renewable energy projects invest more money into the local economy, oil results in lower energy costs for that local economy, so at present there’s a tradeoff to each side. Theoretically when the renewable energy infrastructure grows strong enough to support entire cities the difference in cost between oil and renewable sources like solar energy, wind energy, or biofuels will be negligible, making the choice economically rational and beneficial to all parties.</p>
<p>Some see the increase in a variety of fuel sources as a possible way to spur economic competitiveness in the energy market, which is at present largely controlled by OPEC – a conglomerate of oil producing nations.</p>

<a name="stability"></a>
<p>Some suggest that once a strong infrastructure for renewable energy production is in place then the supply of energy will be more stable than our current system. The reasons given for this are that the renewable energy grid would be</p>
<ol>
<li>Locally produced.</li>
<li>No dependency on one energy source.</li>
<li>The production area and the usage areas would be closer to each other, meaning less transportation of hazardous materials. Furthermore, the current system of large central power stations makes large areas vulnerable to failure in one station.</li>
</ol>
<p>However, some believe that the variable nature of the weather would make renewable energy less reliable.</p>

