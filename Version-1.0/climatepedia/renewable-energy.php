<a name="overview"></a>


<p>
Renewable energy comes from natural sources such as sunlight, geothermal heat, wind, water, and biofuels powered by feedstocks. These sources are considered “renewable” because they are naturally replenished in the normal flow of the world. Oil, coal, natural gas, and nuclear power are not renewable sources of energy.

Although renewable energy sources and consumption have been growing, renewable electricity excluding hydropower still accounts for a small percentage of total energy production and generation.

Increasing oil prices, climate change and global warming concerns, greater government support, and wider citizen support are propelling supportive legislation, which result in better economic incentives that make commercialization more feasible.
</p>
