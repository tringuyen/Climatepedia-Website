<a name="economics"></a>
<h4>Economics of Renewable Energy</h4>
<p>
Capital investment in renewable energy theoretically follows the principle of economies of scale, in that average costs will decrease as production volume increases. This is to say that the more renewable energy that is produced, the lower the cost per unit of that energy is.
</p>
<p>
However, most renewable energy projects – particularly solar energy, wind energy, and geothermal energy, require very high up front capital investments, deterring many investors. Once this initial investment has been made though, materials are generally free. Thus the only costs associated with renewable energy production are maintenance, labor, and regulatory fees.
</p>
<p>
Aside from fears about global warming, <img src="css/images/pedia/solar.jpg" align="left" width="320" height="320" class="wrapleft" alt="solar energy is a great renewable energy source"> the primary driver behind investment in renewable energy is increasing oil costs. As oil becomes more expensive, renewable energy sources become more attractive. For example, take this statistic: “the cumulative capacity-weighted average price of wind power, including the production tax credit, was about 4.4 cents per kilowatt hour in 2009 – a price that competes with fossil fuel-generated electricity.” [1]
</p>
<p>
Many people also point to the renewable energy industry as being a young, innovative market, while the fossil fuels industry is very mature.
</p>
<p>
The industry is particularly suited for developing markets, as production and distribution of fossil fuels can be too expensive in countries without a suitable infrastructure. Furthermore, renewable energy projects in many developing countries have helped lessen poverty, as they provide the energy that businesses need to operate, thus increasing employment.
</p>

<a name="debate"></a>
<h4>Renewable Energy Debate/ Criticisms</h4>
<p>
One of the largest criticisms of renewable energy is that the supply of it is variable and intermittent. However, reliability increases quickly as a stronger foundation is laid. When the sun is covered by clouds in stormy conditions then theoretically the wind generators would be able to collect more energy. Conversely, when it’s a calm sunny day the solar collectors will be generating the majority of the energy. The key behind renewable energy sources is diversification, which will ultimately create a more reliable grid than the one driven by crude oil that we have now.
</p>
<p>
Solar and wind farms have been criticized further on the grounds that neither are aesthetically pleasing, considering that both take up large tracts of land.
</p>
<p>
Biomass and biofuels have been criticized for requiring large amounts of land to harvest the energy. However, the crops used as feedstock for biomass and fuels are also extremely biodiverse and replenish the fertility of the lands, which may be a better alternative when compared to vast strip mines and slag mountains used for coal harvesting.
</p>
<p>
Hydroelectric systems have been criticized for dislocating people who live where the reservoirs are planned, releasing significant amounts of CO2 at construction and flooding the reservoir, disrupting aquatic ecosystems and birdlife, adversely impacting the river environment, creating a risk of sabotage and terrorism, and the very unlikely possibility of catastrophic failure of the dam wall.
</p>
<p>
Some critics of wind farms have cited bird collisions as a drawback, as well as the aforementioned aesthetic concern.
</p>
<p>
Some point out that geothermal energy may not be strictly renewable, as it depends on the rate of extraction not being fast enough to deplete the source. However, if it were depleted it would regenerate over a long period of time.
</p>
<p>
A criticism of the renewable energy industry as a whole is that while the sources may be infinite, the infrastructure is not and will need to be replaced.
</p>

<a name="nuclear"></a>
<h4>Why is Nuclear Power Not Renewable?</h4>
<p>
The current types of nuclear power plants use uranium235 as fuel, of which there is a limited amount on earth. Some have suggested creating “breeder” reactors to create viable alternative fuels, but current safety concerns make this unfeasible. Nuclear fusion has long been the goal for the nuclear power industry, which would open up a source of power as infinite as the sun’s output (that is, in the order of billions of years of supply).
</p>

