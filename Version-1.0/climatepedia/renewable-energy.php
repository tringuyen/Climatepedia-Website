<a name="statistics"></a>
<h4>Statistics</h4>
<p>
The following are interesting statistics about renewable energy that have been pulled from the 2009 Renewable Energy Data Book published in August 2010 by the U.S. Department of Energy. It was produced by Rachel Gelman, edited by Michelle Kubik, and designed by Stacy Buchanan of the National Renewable Energy Laboratory (NREL). The original report
also has a full host of fascinating graphs and tables that we strongly recommend you look at.
</p>

<ul class="list">

<li>Renewable energy accounts for 21% of all global electricity generation when including hydropower, but only 3.8% without hydropower.</li>

<li>Solar PV and wind are the quickest expanding areas of renewable energy. In 2009 92% of renewable energy capacity during the year was in the form of wind energy (when excluding hydropower).</li>

<li>In contrast, the amount of electricity generated from hydropower, biomass, and geothermal sources have remained relatively constant since 2000.</li>

<li>Renewable energy capacity in both the world and in the United States has more than tripled between 2000 and 2009.</li>

<li>Renewable energy represents nearly 12% of total installed capacity and more than 10% of total generation in the United States in 2009.</li>

<li>In 2009, cumulative wind capacity increased by 29% and cumulative solar PV capacity grew nearly 52%.</li>

<li>Between 2000 and 2009, wind energy generation worldwide increased by a factor of almost 9.</li>

<li>In 2009 renewable energy accounted for more than 55% of all new electrical capacity installations in the U.S. – a large contrast from 2004 when all renewable energy captured only 2% of new capacity additions.</li>

<li>(In the U.S.) Between 2000 and 2009, production of corn ethanol increased by a factor of 6, and biodiesel production increased by a factor of more than 100.</li>

<li>Use of ethanol in the United States has also grown substantially, and it accounts for 7.8% of the total U.S. gasoline pool, up from 1% in 2000.</li>

<li>U.S. Energy Production in 2009: Coal – 29.7%, Nuclear – 11.3%, Hydropower – 3.6%, Non-Hydro Renewables – 7.0%, Crude Oil – 15.3%, Natural Gas – 33.0%.</li>

<li>U.S. Energy Consumption in 2009: Coal – 21.1%, Nuclear – 8.8%, Hydropower – 2.8%, Non-Hydro Renewables – 5.4%, Petroleum – 37.1%, Natural Gas – 24.7%.</li>

<li>U.S. Non-Hydro Renewable Energy Production: Biomass – 5.4%, Geothermal – 0.5%, Solar – 0.1%, Wind – 0.9%.</li>

<li>U.S. Non-Hydro Renewable Energy Consumption: Biomass – 4.2%, Geothermal – 0.4%, Solar – 0.1%, Wind – 0.7%.</li>

</ul>

