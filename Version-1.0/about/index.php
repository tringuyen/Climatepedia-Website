<p>



The Climatepedia Organization is a new UCLA student organization dedicated to sharing accurate and reliable climate change information. This website provides the tools for anyone wanting to learn more about climate change-- we have something for Einsteins and average Joes alike.  <br/><br/>

<img src="css/images/group.png" alt="" id="group">

Our mission is to help people understand climate change science, possible solutions, and other issues related to climate change (i.e. impacts to crop yields, infectious disease, water supply, temperature). In order for our readers to better understand climate change, Climate 411 will attempt to resolve questions submitted to us. <br/><br/>    


By incorporating university professors and faculty into our contribution process, our content provides a higher level of expertise and detail than other sources. In addition, they will also reflect actual views on climate change from within the academic community.  In order to adhere to a goal of transparency, we pledge to allow any university professor or faculty the opportunity to contribute to our site as long as we can verify their credentials.

<br/><br/>

Nevertheless we encourage all our readers to ask questions and to be critical of any information they read (whether on our website or others). There's no better way to learn. <br/><br/>

</p>

