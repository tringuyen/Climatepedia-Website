<div class="headlinewrapper"><h3 class="headline"><a href="#" rel="toggle[story3]">Climate Funds Move Beyond Wind, Solar</a>

<a href="#" rel="toggle[story3]" data-openimage="css/images/collapse.png" data-closedimage="css/images/expand.png"><img src="css/images/collapse.png" class="toggleicon"/></a>

</h3></div>

<div id="story3" class="story">
<p class="topcontent">The poor performance of some sectors aiming to slow climate change is pushing money managers to cast further afield for investments that both carry green credentials and are likely to post better returns.</p>

<img src="css/images/newsimage3.jpg" alt="" />

<p class="midcontent">Some renewable-energy stocks, such as those in solar and wind industries, have fallen spectacularly in recent years, belying hopes that they were poised to break out.</p>

<p class="midcontent">"Nobody's questioning the long-term prospects, market share or gains of [renewable energy] sectors, but over the medium [term], it's not been that good," said Vipin Ahuja, manager of Allianz RCM EcoTrends Fund (trading symbol AECOX). "So people are looking elsewhere for sustainable stories for the next couple of years."</p>

<p class="unused"></p>

<p class="bottomcontent">Mr. Ahuja's fund, which he joined about one year ago, is down 19% a year in the past three years, according to data from Morningstar Inc.</p>

