<p class="midcontent">A federal judge initially threw out the case, but the 2nd U.S. Circuit Court of Appeals in New York said it could continue.</p>

<p class="unused"></p>

<p class="bottomcontent">The lawsuit says carbon dioxide is one of the chief causes of global warming. The greenhouse gas is produced when coal, gasoline and other fossil fuels burn. Similar lawsuits are pending in California and North Carolina.</p>

